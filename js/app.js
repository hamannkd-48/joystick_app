function init() {
  window.app = app;

  // Init auth
  authModule.onConfigLoaded = (config) => {
    if (config.currentProfile !== undefined) {
      app.configure.setProfile(config.currentProfile);
    }
    if (config.cursorSpeed !== undefined) {
      state.cursorSpeed = config.cursorSpeed;
      document.getElementById('speedSlider').value = config.cursorSpeed;
      document.getElementById('speedVal').textContent = config.cursorSpeed.toFixed(1);
    }
    if (config.sensors) {
      state.sensors = config.sensors;
      state.nextSensorPin = 4 + config.sensors.length;
      renderSensors();
      renderComponents();
    }
    if (config.buttonMappings) {
      state.buttonMappings = config.buttonMappings;
    }
    if (config.board) {
      document.getElementById('boardSelect').value = config.board;
    }
    window.app.toast('Configuration loaded from your account!', 'success');
  };

  authModule.init((user) => {
    const btn = document.getElementById('authBtn');
    const btnText = document.getElementById('authBtnText');
    if (user) {
      btnText.textContent = user.displayName || user.email || 'Account';
      btn.onclick = () => {
        if (confirm('Sign out?')) authModule.signOutUser();
      };
      const indicator = document.getElementById('saveIndicator');
      indicator.style.display = 'flex';
      indicator.className = 'save-indicator saved';
      document.getElementById('saveText').textContent = 'Auto-save on';
      setTimeout(() => { indicator.style.display = 'none'; }, 3000);
    } else {
      btnText.textContent = 'Sign In';
      btn.onclick = () => app.auth.showModal();
    }
  });

  // Init snake
  initSnake();

  // Start gamepad polling
  startGamepadPolling();
  window.addEventListener('gamepadconnected', () => {
    window.app.toast('Gamepad connected!', 'success');
  });

  // Unsaved work protection
  window.addEventListener('beforeunload', (e) => {
    if (state.dirty && !authModule.user) {
      e.preventDefault();
      e.returnValue = 'You have unsaved work! Sign in to save your configuration.';
    }
  });

  // Set initial profile UI
  app.configure.setProfile(2);
}

init();
