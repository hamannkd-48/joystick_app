let gamepadPollInterval = null;

function startGamepadPolling() {
  if (gamepadPollInterval) return;
  gamepadPollInterval = setInterval(pollGamepads, 16); // ~60fps
}

function pollGamepads() {
  const gamepads = navigator.getGamepads();
  let activeCount = 0;

  for (const gp of gamepads) {
    if (!gp) continue;

    // Buttons
    const buttonMap = [
      'A', 'B', 'X', 'Y', 'LB', 'RB', 'LT', 'RT',
      'View', 'MenuBtn', 'LeftStickPress', 'RightStickPress',
      'DUp', 'DDown', 'DLeft', 'DRight', 'Menu'
    ];

    gp.buttons.forEach((btn, i) => {
      const name = buttonMap[i] || `Btn${i}`;
      const el = document.querySelector(`[data-btn="${name}"]`);
      if (el) {
        if (btn.pressed) {
          el.classList.add('active');
          activeCount++;
        } else {
          el.classList.remove('active');
        }
      }
    });

    // Sticks
    if (gp.axes.length >= 2) {
      const lx = gp.axes[0];
      const ly = gp.axes[1];
      const dot = document.getElementById('leftStickDot');
      if (dot) {
        dot.style.transform = `translate(${lx * 18}px, ${ly * 18}px)`;
        if (Math.abs(lx) > 0.1 || Math.abs(ly) > 0.1) activeCount++;
      }

      // Use left stick for snake if connected
      if (state.snakeRunning && snakeGame) {
        const threshold = 0.3;
        if (Math.abs(lx) > Math.abs(ly)) {
          if (lx > threshold) snakeGame.nextDirection = { x: 1, y: 0 };
          else if (lx < -threshold) snakeGame.nextDirection = { x: -1, y: 0 };
        } else {
          if (ly > threshold) snakeGame.nextDirection = { x: 0, y: 1 };
          else if (ly < -threshold) snakeGame.nextDirection = { x: 0, y: -1 };
        }
      }
    }
    if (gp.axes.length >= 4) {
      const rx = gp.axes[2];
      const ry = gp.axes[3];
      const dot = document.getElementById('rightStickDot');
      if (dot) {
        dot.style.transform = `translate(${rx * 18}px, ${ry * 18}px)`;
        if (Math.abs(rx) > 0.1 || Math.abs(ry) > 0.1) activeCount++;
      }
    }
  }

  document.getElementById('inputCount').textContent = `${activeCount} inputs active`;
}

