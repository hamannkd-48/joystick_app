function onSerialData(line) {
  // Add to serial monitor
  const output = document.getElementById('serialOutput');
  const div = document.createElement('div');

  // Color code
  if (line.startsWith('>>') || line.startsWith('[active]') || line.startsWith('===')) {
    div.className = 'line-info';
  } else if (line.includes('ERROR') || line.includes('FAIL')) {
    div.className = 'line-error';
  }
  div.textContent = line;
  output.appendChild(div);
  output.scrollTop = output.scrollHeight;

  // Parse telemetry: filtX,filtY,enabled
  const parts = line.split(',');
  if (parts.length === 3 && !isNaN(parts[0])) {
    const x = parseInt(parts[0]);
    const y = parseInt(parts[1]);
    const enabled = parseInt(parts[2]);
    updateJoystickMockup(x, y, enabled);
    if (state.snakeRunning) {
      updateSnakeDirection(x, y);
    }
  }
}

