function renderSensors() {
  const container = document.getElementById('addedSensors');
  container.innerHTML = state.sensors.map(s => `
    <div class="added-sensor">
      <div class="sensor-info">
        <span class="sensor-type">${s.type.replace('_', ' ')}</span>
        <span class="sensor-pin">Pin ${s.pin}</span>
      </div>
      <button class="remove-sensor" onclick="app.configure.removeSensor(${s.id})">&times;</button>
    </div>
  `).join('');

  // Also add remap entries for added sensors
  const remapGrid = document.getElementById('remapGrid');
  const existingCustom = remapGrid.querySelectorAll('.remap-custom');
  existingCustom.forEach(el => el.remove());

  state.sensors.forEach(s => {
    const div = document.createElement('div');
    div.className = 'remap-item remap-custom';
    div.innerHTML = `
      <span class="remap-label">${s.type.replace('_', ' ')} (${s.pin})</span>
      <select onchange="app.configure.remapButton('${s.id}', this.value)">
        <option value="mouse_left">Left Click</option>
        <option value="mouse_right">Right Click</option>
        <option value="mouse_middle">Middle Click</option>
        <option value="key_enter">Enter</option>
        <option value="key_space">Space</option>
        <option value="key_esc">Escape</option>
      </select>
    `;
    remapGrid.appendChild(div);
  });
}

function renderComponents() {
  const count = 4 + state.sensors.length;
  document.getElementById('componentCount').textContent = `${count} Components`;

  const list = document.getElementById('componentList');
  // Keep base components, add sensor components
  const sensorHtml = state.sensors.map(s => `
    <div class="component-item">
      <span class="pin">${s.pin}</span>
      <div>
        <div class="label">${s.type.replace('_', ' ').replace(/\b\w/g, c => c.toUpperCase())}</div>
        <div class="desc">Digital input, added sensor</div>
      </div>
    </div>
  `).join('');

  // Remove old dynamic components
  list.querySelectorAll('.dynamic-component').forEach(el => el.remove());
  if (sensorHtml) {
    const wrapper = document.createElement('div');
    wrapper.className = 'dynamic-component';
    wrapper.innerHTML = sensorHtml;
    // Unwrap
    while (wrapper.firstChild) {
      const child = wrapper.firstChild;
      child.classList.add('dynamic-component');
      list.appendChild(child);
    }
  }
}

function generateSketch() {
  let code = SKETCH_TEMPLATE;
  code = code.replace('%%PROFILE%%', String(state.currentProfile));
  code = code.replace(/%%SPEED%%/g, state.cursorSpeed.toFixed(1));

  const mainAction = BUTTON_ACTIONS[state.buttonMappings.joystick_click] || 'MOUSE_LEFT';
  code = code.replace(/%%BUTTON_ACTION%%/g, mainAction);

  // Extra pins
  let extraPins = '';
  let extraSetup = '';
  let extraLogic = '';

  state.sensors.forEach(s => {
    const pinNum = parseInt(s.pin.replace('D', ''));
    extraPins += `const int PIN_${s.type.toUpperCase()} = ${pinNum};\n`;
    extraSetup += `  pinMode(PIN_${s.type.toUpperCase()}, INPUT_PULLUP);\n`;
    const action = BUTTON_ACTIONS[state.buttonMappings[s.id] || 'mouse_left'];
    extraLogic += `    // ${s.type} on ${s.pin}\n`;
    extraLogic += `    if (digitalRead(PIN_${s.type.toUpperCase()}) == LOW) Mouse.press(${action});\n`;
    extraLogic += `    else Mouse.release(${action});\n`;
  });

  code = code.replace('%%EXTRA_PINS%%', extraPins);
  code = code.replace('%%EXTRA_SETUP%%', extraSetup);
  code = code.replace('%%EXTRA_BUTTON_LOGIC%%', extraLogic);

  return code;
}

function markDirty() {
  state.dirty = true;
  authModule.hasUnsavedWork = true;

  // Auto-save if signed in
  if (authModule.user) {
    clearTimeout(autoSaveTimer);
    const indicator = document.getElementById('saveIndicator');
    indicator.style.display = 'flex';
    indicator.className = 'save-indicator saving';
    document.getElementById('saveText').textContent = 'Saving...';

    autoSaveTimer = setTimeout(async () => {
      await authModule.saveConfig({
        currentProfile: state.currentProfile,
        cursorSpeed: state.cursorSpeed,
        profiles: PROFILES,
        sensors: state.sensors,
        buttonMappings: state.buttonMappings,
        board: document.getElementById('boardSelect').value
      });
      state.dirty = false;
      authModule.hasUnsavedWork = false;
      indicator.className = 'save-indicator saved';
      document.getElementById('saveText').textContent = 'Saved';
      setTimeout(() => { indicator.style.display = 'none'; }, 2000);
    }, 1500);
  }
}

