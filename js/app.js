let snakeGame = null;

function initSnake() {
  const canvas = document.getElementById('snakeCanvas');
  const ctx = canvas.getContext('2d');
  const gridSize = 20;
  const cols = Math.floor(canvas.width / gridSize);
  const rows = Math.floor(canvas.height / gridSize);

  snakeGame = {
    canvas, ctx, gridSize, cols, rows,
    snake: [{ x: Math.floor(cols / 2), y: Math.floor(rows / 2) }],
    food: null,
    direction: { x: 1, y: 0 },
    nextDirection: { x: 1, y: 0 },
    score: 0,
    interval: null,
    mouseX: canvas.width / 2,
    mouseY: canvas.height / 2,
    useMouseControl: true
  };

  spawnFood();
  drawSnake();

  // Mouse control for the snake
  canvas.addEventListener('mousemove', (e) => {
    const rect = canvas.getBoundingClientRect();
    snakeGame.mouseX = (e.clientX - rect.left) * (canvas.width / rect.width);
    snakeGame.mouseY = (e.clientY - rect.top) * (canvas.height / rect.height);
  });

  // Keyboard fallback
  document.addEventListener('keydown', (e) => {
    if (!state.snakeRunning) return;
    switch (e.key) {
      case 'ArrowUp':    snakeGame.nextDirection = { x: 0, y: -1 }; break;
      case 'ArrowDown':  snakeGame.nextDirection = { x: 0, y: 1 }; break;
      case 'ArrowLeft':  snakeGame.nextDirection = { x: -1, y: 0 }; break;
      case 'ArrowRight': snakeGame.nextDirection = { x: 1, y: 0 }; break;
    }
  });
}

function spawnFood() {
  if (!snakeGame) return;
  snakeGame.food = {
    x: Math.floor(Math.random() * snakeGame.cols),
    y: Math.floor(Math.random() * snakeGame.rows)
  };
}

function updateSnakeDirection(joystickX, joystickY) {
  if (!snakeGame || !state.snakeRunning) return;
  // Use joystick telemetry to steer snake
  const threshold = 20;
  if (Math.abs(joystickX) > Math.abs(joystickY)) {
    if (joystickX > threshold) snakeGame.nextDirection = { x: 1, y: 0 };
    else if (joystickX < -threshold) snakeGame.nextDirection = { x: -1, y: 0 };
  } else {
    if (joystickY > threshold) snakeGame.nextDirection = { x: 0, y: 1 };
    else if (joystickY < -threshold) snakeGame.nextDirection = { x: 0, y: -1 };
  }
}

function tickSnake() {
  if (!snakeGame) return;
  const g = snakeGame;

  // Mouse-based direction (if not controlled by joystick serial data)
  if (g.useMouseControl && !serialManager?.connected) {
    const head = g.snake[0];
    const headPx = { x: head.x * g.gridSize + g.gridSize / 2, y: head.y * g.gridSize + g.gridSize / 2 };
    const dx = g.mouseX - headPx.x;
    const dy = g.mouseY - headPx.y;
    if (Math.abs(dx) > Math.abs(dy)) {
      g.nextDirection = { x: dx > 0 ? 1 : -1, y: 0 };
    } else {
      g.nextDirection = { x: 0, y: dy > 0 ? 1 : -1 };
    }
  }

  // Prevent reverse
  if (g.nextDirection.x !== -g.direction.x || g.nextDirection.y !== -g.direction.y) {
    g.direction = { ...g.nextDirection };
  }

  const head = g.snake[0];
  const newHead = { x: head.x + g.direction.x, y: head.y + g.direction.y };

  // Wrap around
  if (newHead.x < 0) newHead.x = g.cols - 1;
  if (newHead.x >= g.cols) newHead.x = 0;
  if (newHead.y < 0) newHead.y = g.rows - 1;
  if (newHead.y >= g.rows) newHead.y = 0;

  // Self collision
  if (g.snake.some(s => s.x === newHead.x && s.y === newHead.y)) {
    state.snakeRunning = false;
    clearInterval(g.interval);
    window.app.toast(`Game Over! Score: ${g.score}`, 'error');
    return;
  }

  g.snake.unshift(newHead);

  // Eat food
  if (g.food && newHead.x === g.food.x && newHead.y === g.food.y) {
    g.score++;
    document.getElementById('snakeScore').textContent = g.score;
    spawnFood();
  } else {
    g.snake.pop();
  }

  drawSnake();
}

function drawSnake() {
  if (!snakeGame) return;
  const { ctx, canvas, gridSize, snake, food, cols, rows } = snakeGame;

  // Background
  ctx.fillStyle = '#08080d';
  ctx.fillRect(0, 0, canvas.width, canvas.height);

  // Grid lines
  ctx.strokeStyle = '#1a1a26';
  ctx.lineWidth = 0.5;
  for (let x = 0; x < cols; x++) {
    ctx.beginPath();
    ctx.moveTo(x * gridSize, 0);
    ctx.lineTo(x * gridSize, canvas.height);
    ctx.stroke();
  }
  for (let y = 0; y < rows; y++) {
    ctx.beginPath();
    ctx.moveTo(0, y * gridSize);
    ctx.lineTo(canvas.width, y * gridSize);
    ctx.stroke();
  }

  // Food
  if (food) {
    ctx.fillStyle = '#ef4444';
    ctx.shadowColor = '#ef4444';
    ctx.shadowBlur = 8;
    ctx.beginPath();
    ctx.arc(food.x * gridSize + gridSize / 2, food.y * gridSize + gridSize / 2, gridSize / 2 - 2, 0, Math.PI * 2);
    ctx.fill();
    ctx.shadowBlur = 0;
  }

  // Snake
  snake.forEach((seg, i) => {
    const alpha = 1 - (i / snake.length) * 0.6;
    ctx.fillStyle = i === 0 ? '#818cf8' : `rgba(99, 102, 241, ${alpha})`;
    if (i === 0) {
      ctx.shadowColor = '#6366f1';
      ctx.shadowBlur = 10;
    }
    ctx.fillRect(seg.x * gridSize + 1, seg.y * gridSize + 1, gridSize - 2, gridSize - 2);
    ctx.shadowBlur = 0;
  });
}

